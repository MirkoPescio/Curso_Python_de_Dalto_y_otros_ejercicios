# Ingresando un número entero:
n1 = int(input("Ingrese un número entero: "))
print(n1)

# Ingresando un número flotante:
n2 = float(input("Ingrese un número entero o con decimales: "))
print(n2)

# Concatenación de números:

n3 = input("Ingrese cualquier número: ")
n4 = input("Ingrese otro número: ")
print("Cadena concatenada: " + n3 + n4)

# Multiplicidad de strings:

n5 = input("Ingrese un número: ")
n6 = int(input("Ingrese otro número entero: "))
print("Cadena multiplicada: " + n5 * n6)

