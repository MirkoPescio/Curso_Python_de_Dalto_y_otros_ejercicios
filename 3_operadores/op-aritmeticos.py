# Suma y Resta
suma = 12 + 5
resta = 12 - 5

# Multiplicación y División
multiplicacion = 12 * 5
division = 12 / 5

# Potenciación (exponente) (**)
potenciacion = 12 ** 5

# División baja (redondea el valor de la división) (//)
division_baja = 12 // 5

# Resto o módulo
resto = 12 % 5

print("Resultado de la suma: " + str(suma))
print("Resultado de la resta: " + str(resta))
print("Resultado de la multiplicación: " + str(multiplicacion))
print("Resultado de la división: " + str(division))
print("Resultado de la potenciación: " + str(potenciacion))
print("Resultado de la división entera: " + str(division_baja))
print("Resto de la operación: " + str(resto))

